export const buscarPlantas = (termino: string): Planta[] => {
  const terminoLower = termino.toLowerCase()
  return plantasBCS.filter(
    (planta) =>
      planta.nombre_comun.toLowerCase().includes(terminoLower) ||
      planta.nombre_cientifico.toLowerCase().includes(terminoLower) ||
      planta.familia.toLowerCase().includes(terminoLower),
  )
}

export const obtenerPlantasPorCategoria = (categoria: string): Planta[] => {
  if (categoria === "Todas") return plantasBCS
  return plantasBCS.filter((planta) => planta.categoria === categoria)
}

